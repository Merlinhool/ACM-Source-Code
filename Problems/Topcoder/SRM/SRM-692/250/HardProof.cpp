class HardProof {
        public:
        vector<pair<int, pair<int, int> > > G;
        int n;
        int minimumCost(vector<int> D) {
                n = sqrt(SZ(D) + 0.5);
                G.clear();
                for(int i = 0; i < n; ++i) {
                        for(int j = 0; j < n; ++j) if(i != j) G.pb(mp(D[i*n+j], mp(i, j)));
                }
                if(SZ(G) == 0) return 0;

                sort(ALL(G));
                int ans = 1e9, m = SZ(G), l = 0, r = G[m-1].x - G[0].x, mid;
                while(l <= r) {
                        mid = l + r >> 1;
                        bool flag = 0;
                        for(int i = 0, j; i < m; ++i) {
                                for(j = i+1; j < m && G[j].x - G[i].x <= mid; ++j);
                                if(calc(i, j)) {
                                        flag = 1; break;
                                }
                        }
                        if(flag) {
                                ans = mid;
                                r = mid - 1;
                        } else {
                                l = mid + 1;
                        }
                }
                return ans;
        }
        bool FT[55][55], vis[55];
        bool calc(int l, int r) {
                memset(FT, 0, sizeof FT);
                for(int i = l; i < r; ++i) {
                        FT[G[i].y.x][G[i].y.y] = 1;
                }
                for(int i = 0; i < n; ++i) {
                        memset(vis, 0, sizeof vis);
                        dfs(i);
                        for(int j = 0; j < n; ++j) if(!vis[j]) return 0;
                }
                return 1;
        }
        void dfs(int x) {
                vis[x] = 1;
                for(int i = 0; i < n; ++i) if(!vis[i] && FT[x][i]) dfs(i);
        }
};

